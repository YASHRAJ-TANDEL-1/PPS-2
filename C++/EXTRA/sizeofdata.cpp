#include<iostream>
using namespace std;
int main()
{
     int a;
     char b;
     float c;
     double d;

     
     cout<<"Size of int is "<<sizeof(a)<<endl;
     cout<<"Size of char is "<<sizeof(b)<<endl;
     cout<<"Size of float is "<<sizeof(c)<<endl;
     cout<<"Size of double is "<<sizeof(d)<<endl;
     return 0;  
}